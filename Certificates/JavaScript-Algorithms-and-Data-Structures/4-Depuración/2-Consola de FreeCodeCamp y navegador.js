// Hay muchos métodos para usar con console para mostrar mensajes. log, warn y clear para nombrar algunos. 

    // * Metodos * //

console.clear() // clear () Borra la consola si el entorno lo permite.
console.warn() // warn() Imprime un mensaje de advertencia en la Consola Web.
console.log() // log() Muestra un mensaje en la consola web (o del intérprete JavaScript).


